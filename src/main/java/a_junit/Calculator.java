package a_junit;

public class Calculator {

    public Integer add(Integer a, Integer b) {
        return a + b;
    }

    public Integer subtraction(Integer a, Integer b) {
        return a - b;
    }

    public Integer multiplication(Integer a, Integer b) {
        return a * b;
    }
    public Integer division(Integer a, Integer b) {
        return a / b;
    }



}
